                <h2>Kilka informacji o słowie "podróż" <br></h2>
                <p>
                    Podróż (z łaciny zwana peregrynacją, z francuskiego wojażem) – zmiana miejsca pobytu na odległe. Osoba podróżująca pieszo to wędrowiec, podróżujący środkiem komunikacji to podróżny lub wojażer. W odróżnieniu od nomadów – koczowników, członków grupy ludzi nieposiadającej stałego miejsca zamieszkania, przemieszczającej się z miejsca na miejsce, np. w związku ze zmianami pogody lub w poszukiwaniu żywności, wody, opału albo pastwisk dla zwierząt hodowlanych, podróżnym był od zarania dziejów przedstawiciel cywilizacji osadniczej.

                    Podróże odbywano od dawna, początkowo dla praktycznego celu: nauki, handlu, polityki (poselstwa). Podróżowano również do miejsc kultu religijnego: z chęci odpokutowania za grzechy, zjednania łask lub w podzięce za nie. Podróż dla odwiedzenia miejsc kultu religijnego nazywa się pielgrzymką, a osoba biorąca udział w pielgrzymce to pielgrzym lub pątnik.

                    Podróż wymagała odpowiedniego przygotowania. Podróżni brali ze sobą garderobę na różne pory roku, zastawę stołową, zapasy jedzenia, czasem namioty oraz służbę. W ten sposób podróż stawała się wyprawą.
                    <br><br>
                </p>

                <h2>Podróżujemy całe życie<br /></h2>

                <p>
                    <a id="Pęd">Pęd</a> do poznawania świata stał się jednym z bodźców rozwoju komunikacji. Podróżny stał się pasażerem, a za bagaż z czasem zaczęła wystarczać karta kredytowa.

                    Mniej majętnym, lub mającym mniej czasu musi wystarczyć wycieczka, czyli podróż krótkotrwała. Uczestnik wycieczki to wycieczkowicz. Wycieczkowicz zamiłowany w wycieczkach krajoznawczych to turysta.

                    Podróżowanie dzięki uprzejmości przygodnych właścicieli aut to podróż autostopem, a taki podróżny to autostopowicz.

                    Podróżowanie, ponieważ nie ma się dokąd powrócić, to tułaczka. Według legendy stale w drodze jest Żyd Wieczny Tułacz. Podróż w celu stałego lub czasowego osiedlenia się w innym kraju to emigracja.

                    Podróż w związku z wykonywaną pracą to podróż służbowa, potocznie delegacja. Podróżujący przedstawiciel handlowy to komiwojażer.

                    Okolicznościową odmianą podróży jest podróż poślubna. <br><br>

                </p>
                <h2>Podróże sposobem na życie? Tak, to jest możliwe!</h2>
                <p>
                    Kto z nas jeszcze w czasach dzieciństwa nie marzył o dalekich podróżach, zwiedzaniu najdalszych zakamarków świata i poznawaniu obcych kultur? Zapewne duża część z Was czytała o przygodach Tomka Wilmowskiego w powieściach Alfreda Szklarskiego. Wycieczki w najdalsze zakątki naszego globu wydawały się wtedy tematem odległym, wręcz nierealnym a marzenia o pełnych niespodzianek wycieczkach dla większości z nas zostały po prostu marzeniami. Są jednak osoby, które postanowiły porzucić wygodne życie i udać się w podróż w nieznane.
                </p>
                <p>
                    O tym, że podróże kształcą, przekonywać nie musimy. Wielu naszych klientów udaje się do Niemiec czy Holandii głównie w celach turystycznych, nastawiając się na poznanie krajów zupełnie im obcych, przebywanie w odmiennej kulturze i dotarcie tam, gdzie niewielu Polaków miało okazję. Wszak nawet Niemcy, kraj w którym spotkanie naszego rodaka jest zadaniem relatywnie prostym, kryją w sobie miejsca odwiedzane niezwykle rzadko. Nawet jeżeli nie planujemy poświęcić całego życia podróżom może warto więc choćby krótki urlop przeznaczyć na wycieczkę w nieznane?
                </p>
                <p>
                    Chcąc poczuć klimat podróży w obce nam rejony warto zajrzeć do kilkunastu naprawdę świetnie prowadzonych blogów podróżniczych, w których to autorzy opisują miasta i państwa, do których udało im się dotrzeć. Począwszy od artykułów typowo turystycznych, nastawionych na piękne zabytki, atrakcyjne hotele i wycieczkę z aparatem w ręku po podróże iście survivalowe, z ogromną przygodą w tle.
                    Jeżeli do tej pory nie mieliście okazji śledzić blogów podróżniczych warto zajrzeć choć na chwilę na podroznicy.com.pl, loswiaheros.pl, paragonzpodrozy.pl czy też livealife.pl. Każdy z nich prezentuje inne style, daje nam jednak namiastkę tego jak wygląda życie na walizkach. Zanim więc sami zdecydujemy się na pozostawienie własnego dobytku i podróż busem dookoła świata, warto sprawdzić jak z większością problemów na jakie napotkamy poradzili sobie inni.
                </p>
                <p>
                    Słuchając opowieści naszych klientów wiemy, że wymaga ono przede wszystkim odwagi. O ile jednak czeka nas niewielka wycieczka do jednego z głównych miast Niemiec bądź Holandii, nie musimy przejmować się większością problemów i kwestii wymagających rozwiązania. Jeżeli jednak planujemy zwiedzić te kraje wzdłuż i wszerz, zazwyczaj zbaczając z jasno wytyczonych szlaków, mimo spontanicznej natury warto zaplanować kilka istotnych elementów takich jak chociażby… fundusze.
                    Historie osób, które dotarły na kraniec świata bez grosza w portfelu co prawda poruszają, musimy jednak odpowiedzieć sobie czy jesteśmy gotowi na życie w skrajnych warunkach. Zanim więc zaczniemy wytyczać sobie ambitne szlaki warto w pierwszej kolejności odbyć kilka krótkich podróży, stopniując przede wszystkim trud jaki musimy ponieść w ich trakcie. Jeżeli po ich zakończeniu nadal ciągnęło będzie nas w świat, nic nie stoi na przeszkodzie zacząć spełniać marzenia z dzieciństwa i docierać tam gdzie tylko zapragniemy.
                </p>

